class PrimitiveRanges {

    public static void main(String[] args) {
        System.out.println("Default range for a char datatype is: " + (int) Character.MIN_VALUE + " to " + (int) Character.MAX_VALUE);
        System.out.println("Default range for a byte datatype is: " + Byte.MIN_VALUE + " to " + Byte.MAX_VALUE);
        System.out.println("Default range for an integer datatype is: " + Integer.MIN_VALUE + " to " + Integer.MAX_VALUE);
        System.out.println("Default range for a short datatype is: " + Short.MIN_VALUE + " to " + Short.MAX_VALUE);
        System.out.println("Default range for a long datatype is: " + Long.MIN_VALUE + " to " + Long.MAX_VALUE);
        System.out.println("Default range for a float datatype is: " + Float.MIN_VALUE + " to " + Float.MAX_VALUE);
        System.out.println("Default range for a double datatype is: " + Double.MIN_VALUE + " to " + Double.MAX_VALUE);
    }
}
