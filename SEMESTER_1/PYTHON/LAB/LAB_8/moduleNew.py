class Operations:
    def __init__(self, a, b):
        self.a = a
        self.b = b
    def add(a, b):
        return a+b
    def sub(a, b):
        if a>b:
            return a-b
        else:
            return b-a
    def mul(a, b):
        return a*b
    def div(a, b):
        return a/b
    def pow(a, b):
        return a**b
        
